// 캔버스를 이미지로 변환
var imgData = canvas.toDataURL('image/png');

var imgWidth = 190; // 이미지 가로 길이(mm) / A4 기준 210mm
var pageHeight = imgWidth * 1.414; // 출력 페이지 세로 길이 계산 A4 기준
